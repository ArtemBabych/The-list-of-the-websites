// Our human is 50 years old
let humanAge = 50;
// The first two years score more. So, we need a separate calculation for it.
let earlyYears = 2;
earlyYears *= 10.5;
// Here, we calculate the other years that left from a full number.
let laterYears = humanAge - 2;
// After this calculation we'll know how many years will he have if he were a dog at those years.
laterYears *= 4;
// Let's find out the result for our research. How old would this man be if he were a dog?
hisAgeInDogYears = earlyYears + laterYears;
// Alter to lowercase his name.
let hisName = "Diego".toLowerCase();
// The finish line of our journey will be displayed on the terminal.
console.log(`His name is ${hisName}. He is ${humanAge} years old in human years which is ${hisAgeInDogYears} years old in dog years.`);

